#include "libft.h"

long	ft_pow(long n, size_t i)
{
	size_t	k;
	long	m;

	m = n;
	k = 1;
	if (i == 0)
		return (1);
	while (k < i)
	{
		m = m * n;
		k++;
	}
	return (m);
}
