#include "libft.h"

void	*ft_memset(void *tab, int c, size_t b)
{
	char	*a;
	size_t	k;

	k = 0;
	a = (char *)tab;
	while (k < b)
	{
		a[k] = c;
		k++;
	}
	return (a);
}
