#include "libft.h"

char	*ft_strcpy(char *dst, const char *src)
{
	char	*k;
	size_t	i;

	i = 0;
	k = (char *)src;
	while (k[i])
	{
		dst[i] = k[i];
		i++;
	}
	dst[i] = '\0';
	return (dst);
}
