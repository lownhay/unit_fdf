#include "libft.h"

char	*ft_strncat(char *s1, const char *s2, size_t n)
{
	char	*k;
	size_t	i;
	size_t	j;

	j = 0;
	i = 0;
	k = (char *)s2;
	while (s1[i])
		i++;
	while (k[j] && j < n)
	{
		s1[i] = k[j];
		i++;
		j++;
	}
	s1[i] = '\0';
	return (s1);
}
